package com.redpay.enums;

/**
 * Enum que representa los modos de retiro de dinero de un usuario
 * <p>
 *      <ul>
 *          <li>DAILY: Retiro diario</li>
 *          <li>BIWEEKLY: Retiro cada dos semanas (quincenal)</li>
 *          <li>MONTHLY: Retiro mensual</li>
 *          <li>MANUAL: Retiro manual</li>
 *     </ul>
 * </p>
 */
public enum WithdrawalMode {
    DAILY, BIWEEKLY, MONTHLY, MANUAL
}

